// Arrow functions with parameters 
// example 1
const greeting = (name) =>{
    console.log("Hello"+name);
}
// call the function 
greeting("Diago")
greeting("paul")

// example 2
const mycounty =(county)=>{
    console.log("My county is"+county);
}
// call the function 
mycounty("Nairobi")
mycounty("Naivasha")
// example 3
const addition =(num1,num2)=>{
let answer=num1+num2
console.log("The sum is"+answer)
}
// call the function 
addition(50,100
)

